    public void limpiar() {
        for (int i = 0; i < tblAlumnos.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }
    public void agregarDatos() {
        File archivo = null;
        FileReader fr = null;
        BufferedReader br = null;
        try {
            archivo = new File("Alumnos/Alumnos.txt");
            fr = new FileReader(archivo);
            br = new BufferedReader(fr);
            System.out.println("Leyendo el contendio del archivo.txt");
            String linea;
            int i=0;
            B=new Alumno[40];
            while ((linea = br.readLine()) != null) {
                String[] parts = linea.split("-");
                C = new Materia(parts[2], parts[3], parts[4], parts[5], parts[6]);
                A = new Alumno(parts[0], parts[7], C, Integer.parseInt(parts[1]));
                B[i]=A;
                AñadirTabla(A);
                i++;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (null != fr) {
                    fr.close();
                }
            } catch (Exception e2) {
                e2.printStackTrace();
            }
        }
    }
    public void AñadirTabla(Alumno D) {
        Object T[] = new Object[9];
        T[0] = D.getNombre();
        T[1] = D.getEdad();
        T[2] = D.getNumC();
        T[3] = D.getMat().getUnidad1();
        T[4] = D.getMat().getUnidad2();
        T[5] = D.getMat().getUnidad3();
        T[6] = D.getMat().getUnidad4();
        T[7] = D.getMat().getUnidad5();
        T[8] = D.getMat().getProm();
        modelo.addRow(T);
        
    }
    public void divArreglo(Alumno X[]){
        F=new Alumno[X.length/2];
        G=new Alumno[X.length/2];
        for(int i=0;i<X.length/2;i++){
            F[i]=X[i];
        }
        for(int j=20;j<X.length;j++){
            G[j-20]=X[j];
        }
    }
